/* chmode bit field */
#define CHMODE_MODE(val)		((val) << 0)
#define CHMODE_TXSWRST(val)		((val) << 6)
#define CHMODE_RXSWRST(val)		((val) << 7)
#define CHMODE_TXEN(val)		((val) << 8)
#define CHMODE_TXDRAIN(val)		((val) << 9)
#define CHMODE_RXEN(val)		((val) << 10)
#define CHMODE_GMIILPBK(val)		((val) << 11)
#define CHMODE_TXJABBER(val)		((val) << 12)
#define CHMODE_RXJABBER_L(val)		((val) << 28)

#define CHMODE_RXJABBER(val)		((val) << 0)
#define CHMODE_DISFCS(val)		((val) << 12)
#define CHMODE_INVFCS(val)		((val) << 13)
#define CHMODE_IGNFCS(val)		((val) << 14)
#define CHMODE_STRIPFCS(val)		((val) << 15)
#define CHMODE_IFGLEN(val)		((val) << 16)
#define CHMODE_IFGPACING(val)		((val) << 24)

